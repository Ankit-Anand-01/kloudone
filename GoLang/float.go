//use of floating-point numbers
package main

import "fmt"

func main() {
	//declaration of floating-point numbers
	x := 50.55
	y := 25.25
	//addition of two floating-point numbers
	z := x + y
	fmt.Printf("sum = %f", z)
	//display the type of z variable
	fmt.Printf("\n z is a %T", z)

}
